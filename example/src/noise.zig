fn grad(hash: u8, x: f32, y: f32) f32 {
    const h = hash & 3;
    const u = if (h < 2) x else y;
    const v = if (h < 1 or h == 2) y else x;
    return (if (h & 1 == 0) u else -u) + (if (h & 2 == 0) v else -v);
}

